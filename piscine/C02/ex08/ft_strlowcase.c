char	*ft_strlowcase(char *str)
{
	char	c;
	int		i;

	i = 0;
	c = 'a' - 'A';
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += c;
		i++;
	}
	return (str);
}
