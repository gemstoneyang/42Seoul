int	ft_str_is_uppercase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!('A' <= c && c <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}
